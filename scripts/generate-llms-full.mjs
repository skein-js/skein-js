#!/usr/bin/env node
// Generate `llms-full.txt` — the user-facing docs concatenated into one file an LLM can ingest
// wholesale. This is plain concatenation, not a docs-site build: skein keeps its docs as Markdown on
// GitHub (see AGENTS.md). Run `pnpm docs:llms` after editing any listed doc so the bundle stays fresh.
//
// The curated, ordered reading list below is the "how to use skein" set — overview → getting started →
// guides → recipes. Contributor/design docs (testing, code-practices, roadmap) are intentionally left
// out to keep the bundle focused on consuming skein. Add a file here when it belongs in that story.

import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

/** Ordered reading list, relative to the repo root. Order matters — it's the narrative. */
const DOCS = [
  "README.md",
  "docs/index.md",
  "docs/getting-started.md",
  "docs/using-skein.md",
  "docs/langgraph-cli-compat.md",
  "docs/embedding.md",
  "docs/serving-a-single-graph.md",
  "docs/agent-protocol.md",
  "docs/streaming.md",
  "docs/react-sdk.md",
  "docs/storage.md",
  "docs/runs-and-redis.md",
  "docs/recipes.md",
  "docs/building-an-adapter.md",
  "docs/deploy-railway.md",
  "docs/reuse.md",
];

const HEADER = `# skein-js — full documentation

> A TypeScript Agent Protocol server for LangGraph.js, and a drop-in replacement for the LangGraph CLI.
> This file concatenates the user-facing docs for wholesale ingestion. It is GENERATED by
> scripts/generate-llms-full.mjs — run \`pnpm docs:llms\` to regenerate. The curated index is llms.txt.

`;

const sections = await Promise.all(
  DOCS.map(async (relativePath) => {
    const contents = await readFile(path.join(repoRoot, relativePath), "utf8");
    return `<!-- source: ${relativePath} -->\n\n${contents.trim()}\n`;
  }),
);

const body = HEADER + sections.join("\n\n---\n\n") + "\n";
await writeFile(path.join(repoRoot, "llms-full.txt"), body, "utf8");
console.log(`Wrote llms-full.txt from ${DOCS.length} docs (${body.length} bytes).`);
